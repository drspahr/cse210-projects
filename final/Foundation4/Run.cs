class Run : Activity
{
    // Constructor
    public Run(float dis, float speed, string time, string date) : base(dis, speed, time, date)
    {

    }

    // Method
    public override float CalcSpeed()
    {
        return base.CalcSpeed();
    }

    public override float CalcPace()
    {
        return base.CalcPace();
    }
}